import turtle

# 创建一个乌龟对象
t = turtle.Turtle()

# 设置乌龟的形状为"turtle"
t.shape("turtle")

# 设置乌龟的颜色为绿色
t.color("green")

# 乌龟向前移动100个单位
t.forward(100)

# 乌龟向左转90度
t.left(90)

# 乌龟向前移动100个单位
t.forward(100)

# 乌龟向左转90度
t.left(90)

# 乌龟向前移动100个单位
t.forward(100)

# 乌龟向左转90度
t.left(90)

# 乌龟向前移动100个单位
t.forward(100)

# 结束绘制
turtle.done()
